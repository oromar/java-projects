    private void btnSalvarEntregadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarEntregadorActionPerformed

        if (editMode) {
            try {
                getDadosTela();                
                Fachada.getInstancia().atualizar(entregadorToEdit);
                JOptionPane.showMessageDialog(this, "Registro atualizado com sucesso !", "Info", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }

        } else {

            try {
                getDadosTela();
                Fachada.getInstancia().inserir(entregadorToEdit);
                JOptionPane.showMessageDialog(this, "Registro inserido com sucesso !", "Info", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSalvarEntregadorActionPerformed

    private void getDadosTela() throws Exception {
        entregadorToEdit = new Entregador();
        entregadorToEdit.setCpf(edtCpfEntregador.getText());
        entregadorToEdit.setDateString(edtDataNascimentoEntregador.getText());
        entregadorToEdit.setMatricula(edtMatriculaEntregador.getText());
        entregadorToEdit.setNome(edtNomeEntregador.getText());
        entregadorToEdit.setModelo(edtModeloMotoEntregador.getText());
        entregadorToEdit.setTelefone(edtTelefoneEntregador.getText());
        entregadorToEdit.setPlacaMoto(edtPlacaMotoEntregador.getText());
        entregadorToEdit.validar();
    }
    
